const defaultLang = "en";
const rtlLanguages = new Set(["ar"]);
const languageSelect = document.getElementById("languageSelect");
const languageTriggers = document.querySelectorAll("[data-language-trigger]");

const applyLanguage = (lang) => {
  const safeLang = translations[lang] ? lang : defaultLang;
  const dict = translations[safeLang];
  const defaultDict = translations[defaultLang];

  document.documentElement.lang = safeLang;
  document.documentElement.dir = rtlLanguages.has(safeLang) ? "rtl" : "ltr";
  document.title = dict.title || defaultDict.title;

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    const value = dict[key] || defaultDict[key];
    if (value) {
      node.textContent = value;
    }
  });

  if (languageSelect) {
    languageSelect.setAttribute(
      "aria-label",
      dict.languageLabel || defaultDict.languageLabel
    );
    languageSelect.value = safeLang;
  }
};

const savedLanguage = localStorage.getItem("koda-language");
const initialLanguage =
  savedLanguage || (navigator.language || defaultLang).slice(0, 2);
applyLanguage(initialLanguage);

if (languageSelect) {
  languageSelect.addEventListener("change", () => {
    const nextLang = languageSelect.value;
    localStorage.setItem("koda-language", nextLang);
    applyLanguage(nextLang);
  });
}

if (languageSelect && languageTriggers.length > 0) {
  languageTriggers.forEach((trigger) => {
    trigger.addEventListener("click", (event) => {
      event.preventDefault();
      window.scrollTo({ top: 0, behavior: "smooth" });
      languageSelect.focus();

      if (typeof languageSelect.showPicker === "function") {
        languageSelect.showPicker();
      }
    });
  });
}

document.querySelectorAll('a[href^="#"]').forEach((anchor) => {
  anchor.addEventListener("click", (event) => {
    const targetId = anchor.getAttribute("href");
    if (!targetId || targetId === "#") return;

    const target = document.querySelector(targetId);
    if (!target) return;

    event.preventDefault();
    target.scrollIntoView({ behavior: "smooth", block: "start" });
  });
});
